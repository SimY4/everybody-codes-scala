object Quest3:
  def solve(input: String, directions: List[Direction] = Direction.hvOnly): Int =
    val coords = input.linesIterator.zipWithIndex
      .flatMap: (line, y) =>
        line.indices.filter(line(_) == '#').map(Coord(_, y))
      .toSet

    Iterator
      .iterate(coords): coords =>
        coords.filter: coord =>
          directions.forall(d => coords.contains(coord + d.coord))
      .takeWhile(_.nonEmpty)
      .map(_.size)
      .sum

  def solve2(input: String): Int = solve(input)

  def solve3(input: String): Int = solve(input, Direction.values.toList)

