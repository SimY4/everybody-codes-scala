  def solve(input: String): Int =
    val l1 :: _ :: l2 :: Nil = input.linesIterator.toList
    val words                = l1 match
      case s"WORDS:$words" => words.split(",").mkString("(?=", "|", ")").r

    words.findAllIn(l2).size

  def solve2(input: String): Int =
    val l1 :: _ :: lines = input.linesIterator.toList
    val words            = l1 match
      case s"WORDS:$words" =>
        words.split(",").flatMap(word => Seq(word, word.reverse)).sortBy(-_.length).mkString("(?=(", "|", "))").r

    lines
      .map: line =>
        words.findAllMatchIn(line).flatMap(m => m.start until m.start + m.group(1).length).distinct.size
      .sum

  def solve3(input: String): Int =
    val l1 :: _ :: lines = input.linesIterator.toList
    val words            = l1 match
      case s"WORDS:$words" =>
        words.split(",").flatMap(word => Seq(word, word.reverse)).sortBy(-_.length).mkString("(?=(", "|", "))").r

    val fst = for
      (line, i) <- lines.zipWithIndex
      m         <- words.findAllMatchIn(line + line)
      if 0 <= m.start && m.start < line.length
      j <- m.start until m.start + m.group(1).length
    yield (i, j % line.length)
    val snd = for
      (line, j) <- lines.transpose.map(_.mkString).zipWithIndex
      m         <- words.findAllMatchIn(line)
      i         <- m.start until m.start + m.group(1).length
    yield (i, j)

    (fst ::: snd).distinct.size

